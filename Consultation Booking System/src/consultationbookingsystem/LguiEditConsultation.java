    public void setUser(Lecturer x, Consultation z){
        current = x;
        consultation = z;
     
        for(int i=0; i<current.getCons().size(); i++){
            Consultation zz = current.getCons().get(i);
            
            if(consultation.getDay().equals(zz.getDay()) && consultation.getTime().equals(zz.getTime()) && consultation.getVenue().equals(zz.getVenue())){
                lblVenue.setText(zz.getVenue());
                String date = zz.getDay();
                                                    
                try{        
                                    
                    SimpleDateFormat dateFormatterrr = new SimpleDateFormat("E, y-M-d 'at' h:m:s a z");   
                    dateFormatterrr = new SimpleDateFormat("d/M/y");     
                    Date bb = dateFormatterrr.parse(date);             
                    dateChooser.setDate(bb); 
                                    
                }catch(ParseException pe){
                                    
                }
                
                cboTime.setSelectedItem(zz.getTime());
                
            }
        }
    }
    
