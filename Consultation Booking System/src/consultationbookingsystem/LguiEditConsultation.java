    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            DateFormat dff = new SimpleDateFormat("d/M/yyyy");
            String newVenue = lblVenue.getText();
            String newDate = dff.format(dateChooser.getDate());
            String newTime = cboTime.getSelectedItem().toString();
            boolean abc = true;

            SimpleDateFormat dateFormatterrr = new SimpleDateFormat("E, y-M-d 'at' h:m:s a z");
            dateFormatterrr = new SimpleDateFormat("d/M/y");
            DateFormat df = new SimpleDateFormat("d/M/yyyy");
            String a = df.format(dateChooser.getDate());
            Date bb = dateFormatterrr.parse(a);
            Date ab = new Date();

            if(bb.before(ab)){
                JOptionPane.showMessageDialog(this, "SELECT THE DATE BEFORE TODAY!","PLEASE SELECT THE DATE BEFORE TODAY!", JOptionPane.ERROR_MESSAGE);
            }else if(newVenue.trim().isEmpty() || newDate.trim().isEmpty() || newTime.trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "PLEASE FILL ALL THE FIELD!","PLEASE FILL ALL THE FIELD!", JOptionPane.ERROR_MESSAGE);
            }
            else{
                for(int i = 0; i<current.getCons().size(); i++){
                    
                    x = current.getCons().get(i);
                    
                    if(newVenue.equals(x.getVenue()) && newTime.equals(x.getTime()) && newDate.equals(x.getDay())){
                        JOptionPane.showMessageDialog(this, "THE CONSULTATON IS EXIST!","THE CONSULTATON IS EXIST!", JOptionPane.INFORMATION_MESSAGE);
                        abc = false;
                        break;
                    }else if(newTime.equals(x.getTime()) && newDate.equals(x.getDay())){
                        JOptionPane.showMessageDialog(this, "THE TIME & DATE GOT CONSULTATION!","THE TIME & DATE GOT CONSULTATION!", JOptionPane.INFORMATION_MESSAGE);
                        abc = false;
                        break;
                    }else{
                        abc = true;
                    }
                }
                if(abc == true){
                    for(int i = 0; i<current.getCons().size(); i++){
                        Consultation y = current.getCons().get(i);
                                            
                        if(consultation.getDay().equals(y.getDay()) && consultation.getTime().equals(y.getTime()) && consultation.getVenue().equals(y.getVenue())){
                            consultation.setDay(dateFormatterrr.format(bb));  
                            consultation.setTime(newTime); 
                            consultation.setVenue(newVenue);
                            JOptionPane.showMessageDialog(this, "EDIT SUCCESSFULLY!","EDIT SUCCESSFULLY!", JOptionPane.INFORMATION_MESSAGE);
                            break;
                        }
                    }
                }
            }

        }catch(ParseException cd){
            JOptionPane.showMessageDialog(this, "DATE FAILED TO CONVERT!","DATE FAILED TO CONVERT!", JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "PLEASE SELECT A DATE!","PLEASE SELECT A DATE!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

