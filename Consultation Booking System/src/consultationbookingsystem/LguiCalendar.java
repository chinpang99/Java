    public void getData(){
        Consultation a = null;
        
            for(int i = 0; i<current.getCons().size(); i++)
            {                                           
                consultation_class = current.getCons().get(i);
                
                if(consultation_class.getDay().contains(c)){
                    a = consultation_class;
                    break;
                }
            }     
            if(a == null){
                JOptionPane.showMessageDialog(null,"There is not Consultation & Appointment on " + c);
            }else{
                appointmentt.setVisible(true);
                appointmentt.setUser(current, a);
                appointmentt.setBoolean(true); 
                this.setVisible(false);
            }

    }
    
